int	main(int ac, char **av)
{
	int	a;
	int	b;
	int	c;

	if (ac == 3)
	{
		a = atoi(av[1]);
		b = atoi(av[2]);
		if (a > 0 && b > 0)
		{
			while (b != 0)
			{
				c = b;
				b = a % b;
				a = c;
			}
			printf("%d", a);
		}
	}
	write(1, "\n", 1);
	return (0);
}