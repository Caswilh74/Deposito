#include <unistd.h>

int	main(int ac, char **av)
{
	int	i = 0;
	int	flag = 0;

	if (ac == 2)
	{
		while (av[1][i] == ' ' || (av[1][i] >= 9 && av[1][i] <= 13))
			i++;
		while (av[1][i])
		{
			if (av[1][i] == ' ' || (av[1][i] >= 9 && av[1][i] <= 13))
				flag = 1;
			if (av[1][i] != ' ' && (av[1][i] >= 33 && av[1][i] <= 126))
			{
				if (flag == 1)
					write(1, "   ", 3);
				write(1, &av[1][i], 1);
				flag = 0;
			}
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}